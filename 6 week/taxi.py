"""
После затянувшегося совещания директор фирмы решил заказать такси, чтобы
развезти сотрудников по домам. Он заказал N машин — ровно столько, сколько
у него сотрудников. Однако когда они подъехали, оказалось, что у каждого
водителя такси свой тариф за 1 километр.

Директор знает, какому сотруднику сколько километров от работы до дома (к
сожалению, все сотрудники живут в разных направлениях, поэтому нельзя
отправить двух сотрудников на одной машине). Теперь директор хочет определить,
сколько придется заплатить за перевозку всех сотрудников. Естественно,
директор хочет заплатить как можно меньшую сумму.

Формат ввода
В первой строке записаны N чисел через пробел, задающих расстояния в
километрах от работы до домов сотрудников компании. Во второй строке
записаны N чисел — тарифы за проезд одного километра в такси.

Формат вывода
Выведите одно целое число — наименьшую сумму, которую придется заплатить за
доставку всех сотрудников.
"""


dist = sorted([int(i) for i in input().split()])
price = sorted([int(i) for i in input().split()], reverse=True)
print(sum([dist[i] * price[i] for i in range(len(dist))]))
